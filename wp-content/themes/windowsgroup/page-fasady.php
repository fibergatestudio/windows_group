                </p>
                <p>
                    Профиль холодного типа применяется на неотапливаемых объектах, в частности промышленных. Используется при обустройстве витрин, балконов, в виде мансардных окон.
                </p>
                <p>
                    Холодный профиль является однокамерной системой без дополнительного уплотнителя. Характеризуется меньшим весом, использованием недорогой фурнитуры. Эти факторы снижают стоимость алюминиевых изделий.
                </p>
                <p>
