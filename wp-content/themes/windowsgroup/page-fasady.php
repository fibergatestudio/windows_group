                <p>Фасадные алюминиевые системы бывают нескольких видов:</p>
                <ul>
                    <li>·         стоечный ригельный фасад;</li>
                    <li>·         структурный фасад;</li>
                    <li>·         полуструктурный фасад.</li>
                </ul>
                <p>
                    Стоечно-ригельный вид является экономичным, прочным и надежным в сравнении с другими типами алюминиевой отделки. Отличается простотой установки. Основу конструкции составляет стоечно-ригельный каркас. Между профилем и каркасом монтируется стеклопакет. Для герметичности и теплозащиты элементы соединяются резиновым уплотнителем.
                </p>
                <p>
                    Стоечная ригельная фасадная система позволяет использовать светопрозрачную конструкцию высотой не более 3 м. Дополнительно выполняются различные вершины и изломы, примыкания с основным сооружениям или к встроенным группам, применять поворотные откидные окна.
                </p>
                <p>
                    Принцип структурного фасадного типа аналогичен стоечному ригельному типу. Различие заключается в том, что в структурном типе размер внешнего стекла больше внутреннего, чем в каркасных ячейках. Поэтому снаружи вставки монтируются плотно. Наружные прижимные элементы крепления отсутствуют, а стеклопакеты фиксируются с помощью силиконового герметика.
                </p>
                <p>
                    Полуструктурный тип отделки – оптимальное решение в выборе между структурным и обычным остеклением. Наружное стекло бывает обычным, тонированным, закаленным. В фасаде присутствуют прижимные планки, декоративные крышки для ригелей и стоек.
                </p>
                <p>
                    Полуструктурная система применяется, когда необходимо застеклить фасад полностью, но большие стеклопакеты не подходят по дизайну. Эта технология позволяет встраивать скрытые оконные створки, открывающиеся наружу.
                </p>

