                  <h2 class="title-post">Уход за стеклопакетом</h2>
                  <p>Очистить 3х-камерный стеклопакет от накопившейся пыли и грязи можно обычным мыльным или содовым раствором. Запрещено применять абразивные моющие вещества. Нельзя тереть поверхность жесткими губками и щетками из-за риска повреждений и царапин. Лучше всего использовать мягкую салфетку или ткань. При сильном загрязнении допускается применение специальных очистителей.</p>
